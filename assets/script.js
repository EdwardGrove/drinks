function openPopup() {
    var leftPos = (screen.width - 400) / 2;
    var topPos = (screen.height - 600) / 2;
    var popup = window.open('popup.html', 'popup', 'toolbar=no, location=no, directories=no, status=no, menubar=no, scrollbars=yes, resizable=yes, width=400, height=600, top=' + topPos + ', left=' + leftPos);
  }
  
